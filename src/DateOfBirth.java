
public class DateOfBirth {
	//instance variables
	private int day;
	private int month;
	private int year;
	
	//constructor
	public DateOfBirth(int day, int month, int year){
		this.day = day;
		this.month = month;
		this.year = year;
		
	}

	//getters and setters
	public int getDay() {
		return this.day;
	}
	
	public void setDay(int day) {
		this.day = day;
	}
	
	public int getMonth() {
		return this.month;
	}
	
	public void setMonth(int month) {
		this.month =month;
	}
	
	public int getYear() {
		return this.year;
	}
	
	public void setYear(int year) {
		this.year = year;
	}
	

}
